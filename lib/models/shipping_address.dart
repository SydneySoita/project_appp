class ShippingAddress{
  final String id;
  final String firstName;
  final String lastName;
  final String address;
  final String additionalInfo;
  final String region;
  final String city;
  final String phoneNumber;
  final String additionalPhoneNumber;

  ShippingAddress({
    this.id,
    this.firstName,
    this.lastName,
    this.address,
    this.additionalInfo,
    this.region,
    this.city,
    this.phoneNumber,
    this.additionalPhoneNumber,
  });
}